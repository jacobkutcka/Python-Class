from turtle import *
from math import *

def main():
    # Don't change this block -------------------------------------------------
    setup(564, 564)
    width('5')
    # -------------------------------------------------------------------------


    # Write your mainline logic here ------------------------------------------
    pendown()
    for i in range(360):
        rad = pi * i / 180
        x = (rad/10)*cos(rad)
        y = (rad/10)*sin(rad)
        goto(6*x,6*y)

# Don't change this -----------------------------------------------------------
if __name__ == '__main__':
    main()
    done()
